package org.chirvascristina.operationsandciclicfunctions;

public class AritmeticOperationExample {
    public static void main(String[] args) {
        int a = 43;
        int c = 3;

        //Sum
        int sum = a + c;
        System.out.println("Sum1: " + sum);
        System.out.println("Sum2: " + (a+c));

        //Substraction
        int sub = a - c;
        System.out.println("Substraction: " + sub);


        //Multiplication
        int multiplication = a * c;
        System.out.println("Multiplication: " + multiplication);

        //Devision
        int devision = a / c;
        System.out.println("Devision: " + devision);

        //The rest
        int rest = a % c;
        System.out.println("The rest: " + rest);

    }
}
